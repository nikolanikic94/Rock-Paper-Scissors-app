// LOCAL STORAGE
let countWins = () => {
    if (typeof(Storage) !== "undefined") {
      if (localStorage.clickcount) {
        localStorage.clickcount = Number(localStorage.clickcount)+1;
      } else {
        localStorage.clickcount = 1;
      }
      scoreNumber.innerHTML = localStorage.clickcount;
    } else {
      scoreNumber.innerHTML = "Sorry, your browser does not support web storage...";
    }
  }

function rememberCount() {
    let storedNumber = localStorage.getItem('clickcount');
    let placeholder = 0;

    if (storedNumber === null) {
        scoreNumber.innerHTML = placeholder;
    } else {
        scoreNumber.innerHTML = storedNumber;
    }
}

